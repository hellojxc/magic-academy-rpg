def write_report(
    meshes: list[bpy.types.Object],
    armature: bpy.types.Object,
    replaced_source_parts: list[dict[str, Any]],
    cleanup_report: dict[str, Any],
    authored_arm_parts: list[bpy.types.Object],
) -> None:
    face = next((obj for obj in meshes if "Face_Head" in obj.name), None)
    face_morphs = list_morph_targets(face)
    morph_meshes = [obj.name for obj in meshes if list_morph_targets(obj)]
    part_names = [obj.name for obj in meshes]
    total_faces = sum(len(obj.data.polygons) for obj in meshes)
    binding = summarize_binding(meshes, armature)
    secondary = v25.v17.v12.summarize_secondary_bones(armature)
    animations = [action.name for action in bpy.data.actions]
    torso_removed_faces = sum(item.get("removedFaces", 0) for item in cleanup_report.values())
    torso_preserved = any(item.get("skipped") for item in cleanup_report.values())
    acceptance_checks = {
        "keepsV17VisualCore": all(
            any(part in name for name in part_names)
            for part in ["Face_Head", "Hair_Back", "Hair_LeftLock", "Hair_RightLock", "Torso_Camisole", "Skirt_Center", "Legs", "Shoes"]
        ),
        "preservesSourceTorsoAfterCleanupRegression": torso_preserved and torso_removed_faces == 0,
        "replacesFragmentedSourceArms": bool(replaced_source_parts)
        and not any(name == "MatureSenpai_V26_Arms_Hands" for name in part_names),
        "hasHigherDensityAnatomicalArmHands": len(authored_arm_parts) == 2
        and all(len(obj.data.polygons) >= 400 for obj in authored_arm_parts),
        "keepsFaceOnlyExpressionMorphs": face is not None and morph_meshes == [face.name] and sorted(face_morphs) == sorted(MORPH_NAMES),
        "keepsHairSecondaryBones": len(secondary["hair"]) >= 6,
        "keepsSkirtSecondaryBones": len(secondary["skirt"]) >= 5,
        "keepsDeformationQaClips": any(name.startswith("v17_deformation_stress") for name in animations)
        and any(name.startswith("v17_secondary_sway_test") for name in animations),
        "hasWeightedRuntimeMeshes": binding["zeroWeightVertices"] == 0,
        "webRuntimeBudgetTarget": total_faces <= 65000,
    }
    report = {
        "assetId": "mature-senpai-commercial-v26",
        "characterId": "mature_senpai",
        "status": "stable-source-torso-and-anatomical-hands-preview",
        "source": "public/assets/models/mature_senpai_commercial_v17.glb",
        "blendSource": "assets/characters/mature_senpai/source/mature_senpai_commercial_v26.blend",
        "output": "public/assets/models/mature_senpai_commercial_v26.glb",
        "thumbnail": "public/assets/models/mature_senpai_commercial_v26.png",
        "generatedBy": "remote Blender headless + assets/characters/mature_senpai/tools/build_final_v26.py",
        "runtime": {
            "defaultAsset": True,
            "format": "glb",
            "url": "/assets/models/mature_senpai_commercial_v26.glb",
            "thumbnailUrl": "/assets/models/mature_senpai_commercial_v26.png",
            "fallbackAssetId": "mature-senpai-commercial-v17",
            "threeRuntimeFeatures": [
                "v17 face, hair, skirt, legs, shoes, choker, expression layer, and QA clips",
                "source Torso_Camisole preservation after destructive cleanup exposed shoulder seam artifacts",
                "higher-density procedural anime arms and segmented hands",
                "dedicated Face_Head morphTargetInfluences",
                "Secondary_* bone spring overlay",
            ],
        },
        "productionFlags": {
            "physicalRuntimeSplitParts": True,
            "dedicatedFaceExpressionMorphs": True,
            "sourcePreservedV17VisualCore": True,
            "sourceTorsoPreservedForStablePreview": torso_preserved,
            "legacyTorsoArmSleeveFragmentsRemoved": False,
            "fragmentedSourceArmsRemoved": True,
            "higherDensityAnatomicalHands": True,
            "hairSecondaryBones": True,
            "skirtSecondaryBones": True,
            "webRuntimeReady": True,
        },
        "runtimeParts": summarize_parts(meshes),
        "replacedSourceParts": replaced_source_parts,
        "torsoCleanup": cleanup_report,
        "authoredArmHandParts": [obj.name for obj in authored_arm_parts],
        "morphTargets": {face.name: face_morphs} if face else {},
        "armatureBones": [bone.name for bone in armature.data.bones],
        "secondaryRig": secondary,
        "binding": binding,
        "animationClips": animations,
        "acceptanceChecks": acceptance_checks,
        "runtimeDecision": {
            "defaultAsset": True,
            "fallbackAssetId": "mature-senpai-commercial-v17",
            "reason": "v26 is the current stable preview default: it keeps the visually stable v17 source torso after destructive torso cleanup exposed shoulder seams, while replacing the fragmented source arms with higher-density procedural anime arms/hands. v17 remains the deformation-production fallback.",
        },
        "limitations": [
            "v26 still uses a procedural arm/hand reconstruction rather than a sculptor-authored full quad retopology.",
            "The inherited source torso still contains some sleeve/arm artifact texture islands; a destructive automated cleanup was rejected because it exposed worse shoulder seams.",
            "The replacement arms use procedural materials and will need UV/texture repaint to perfectly match the Hunyuan atlas.",
            "Skirt panel texture islands remain source-preserved; final commercial polish still needs an artist cloth-retopo and brush-painted secondary weights.",
        ],
    }
    OUTPUT_REPORT.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT_REPORT.write_text(json.dumps(report, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")


def main() -> dict[str, Any]:
    clean_scene()
    meshes, armature, replaced_source_parts, cleanup_report = import_clean_v17_source()
    authored_arm_parts = add_v26_anime_arm_hand_meshes(armature)
    meshes.extend(authored_arm_parts)
    face = next((obj for obj in meshes if "Face_Head" in obj.name), meshes[0])
    v25.v17.v12.render_preview(face, meshes)
    v25.v17.export_outputs(meshes, armature)
    write_report(meshes, armature, replaced_source_parts, cleanup_report, authored_arm_parts)
    return {
        "glb": str(OUTPUT_GLB),
        "png": str(OUTPUT_PNG),
        "blend": str(OUTPUT_BLEND),
        "report": str(OUTPUT_REPORT),
        "runtimeParts": len(meshes),
        "torsoCleanup": cleanup_report,
        "authoredArmHandParts": [obj.name for obj in authored_arm_parts],
    }


if __name__ == "__main__":
    print(json.dumps(main(), indent=2))
